    public function send($phone,$message){

        $params = array(
            'to' => $phone,
            'message' => $message,
            'from' =>  env('AT_SENDERID'),
            'enqueue' => 1,
        );
        $AT = new AfricasTalking(env('AT_SMS_USERNAME'), env('AT_API_KEY'));

        return $AT->sms()->send($params);
    }

